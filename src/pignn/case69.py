VN_KV = 12.66
SN_MVA = 10.0


def case69():
    """Build the 69-bus feeder as a pandapower network (bus i-1 = published bus i)."""
    net = pp.create_empty_network(name="case69", sn_mva=SN_MVA)
    for i in range(69):
        pp.create_bus(net, vn_kv=VN_KV, name=f"bus_{i + 1}", index=i)
    pp.create_ext_grid(net, bus=0, vm_pu=1.0)

    for f, t, r, x, p_kw, q_kvar in _BRANCHES:
        pp.create_line_from_parameters(
            net, from_bus=f - 1, to_bus=t - 1, length_km=1.0,
            r_ohm_per_km=r, x_ohm_per_km=x, c_nf_per_km=0.0, max_i_ka=0.4)
        if p_kw or q_kvar:
            pp.create_load(net, bus=t - 1, p_mw=p_kw / 1e3, q_mvar=q_kvar / 1e3)

    for f, t, r, x in _TIES:
        pp.create_line_from_parameters(
            net, from_bus=f - 1, to_bus=t - 1, length_km=1.0,
            r_ohm_per_km=r, x_ohm_per_km=x, c_nf_per_km=0.0, max_i_ka=0.4,
            in_service=False)
    return net
